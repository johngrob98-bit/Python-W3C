# Dictionaries
#-------------------------------------------------------------------------------------------------------
# Grundlegendes
# Snytax: 
dict = {
    "brand" : "Ford",
    "model" : "Mustang",
    "year" : 1964
}
print(dict)
# In Endeffekt wie der Name schon sag "Wörterbuch" (nicht wörtlich nehmen) Aber eine Sammlung von Daten
# und Elementen welche Werte, Namen, Zuordnungen behausen
# Die Syntax ist wie oben dict dann {} und es ist Aufgeteilt in Schlüssel:Werte paare key:value
# Dictionary sind 1. Geordnet 2. Veränderbar 3. Erlauben keine Duplikate

#-------------------------------------------------------------------------------------------------------
# Elemente von Dictionaries
# Wie schon oben: geordnet, veränderbar und keine Duplikate
# -> Elemente können per [] referenziert werden
print(dict["brand"]) # Ausgabe hierbei eben nur die Marke, funktioniert mit allen anderen Eigenschaften 

#-------------------------------------------------------------------------------------------------------
# Sortiert oder Unsortiert -> Kurze Auffrischung 
# Sortiert (orderd) -> definierte Reihenfolge, !ändert! sich nicht
# Unsortier (unorderd) -> keine definierte Reihenfolge, nicht refenzierbar per Index
#-------------------------------------------------------------------------------------------------------
# Änderbar: Elemente können hinzugefügt und entfernt werden, nach Erstellung
#-------------------------------------------------------------------------------------------------------
# Keine Duplikate!
